package semantic;

/**
 * Статический тип выражения (выводится семантическим анализатором).
 *
 * miniJS — динамически типизируемый язык, поэтому большинство выражений
 * имеют тип ANY. Конкретные типы выводятся только там, где это очевидно
 * статически: литералы, унарные/бинарные операции над литералами и т.п.
 * Эта информация нужна для свёртки констант и проверки литеральных
 * арифметических ошибок (вроде null + null).
 */
public enum Type {
    NUMBER,
    STRING,
    BOOLEAN,
    NULL,
    UNDEFINED,
    FUNCTION,
    ARRAY,
    OBJECT,
    ANY
}
