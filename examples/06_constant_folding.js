// Свёртка констант — должно остаться по одному литералу
let a = 2 + 3 * 4;             // -> 14
let b = (10 - 4) / 2;          // -> 3
let c = "Hello, " + "World";   // -> "Hello, World"
let d = "n=" + 42;             // -> "n=42"
let e = !false;                // -> true
let f = -7 + 10;               // -> 3
let g = 5 < 10;                // -> true

console.log(a, b, c, d, e, f, g);
